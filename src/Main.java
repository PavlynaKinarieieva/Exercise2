public class Main {
    public static void main(String[] args) {
        add();
        subtract();
        divide();
        multiply();
        }
        public static void add() {
           int number1 = 20;
           int number2 = 10;

           int result = number1 + number2;
           System.out.println(result);
        }
        public static void subtract() {
            int number1 = 20;
            int number2 = 10;

            int result = number1 - number2;
            System.out.println(result);
        }
        public static void divide() {
            int number1 = 20;
            int number2 = 10;

            int result = number1 / number2;
            System.out.println(result);
        }
        public static void multiply() {
            int number1 = 20;
            int number2 = 10;

            int result = number1 * number2;
            System.out.println(result);
    }
}